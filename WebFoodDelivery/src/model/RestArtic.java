package model;

public class RestArtic {

	private int idR;
	private int idA;
		
	public RestArtic(int idR, int idA) {
		super();
		this.idR = idR;
		this.idA = idA;
	}
	
	public int getIdR() {
		return idR;
	}
	public void setIdR(int idR) {
		this.idR = idR;
	}
	public int getIdA() {
		return idA;
	}
	public void setIdA(int idA) {
		this.idA = idA;
	}
	
	
}
